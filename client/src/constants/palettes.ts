/**
 * Fusiey bead palettes — 2.6mm mini beads.
 *
 * The full 221-colour chart is the single source of truth, imported from the
 * official spreadsheet (private_ref/221_colour.xlsx). Codes are grouped into
 * 9 families:
 *   A yellow/orange · B green · C blue/cyan · D purple/violet · E pink
 *   F red · G beige/brown · H neutrals · M muted/dusty
 *
 * Colour names are generated deterministically from the hex (see autoName) —
 * the CODE is the canonical identifier customers see on bags and BOM tables.
 *
 * Future sets (e.g. a 144-colour kit) slot in as additional PALETTES entries.
 */

// ── semantic ID lists (kept from MARD-221 spec) ──────────────────────
// isGrey excludes H1 (pure white) and H2 (#FBFBFB, effectively white) — grey
// removal should not touch highlight-level whites.
const SKIN_IDS = new Set(['E2', 'E8', 'G1', 'G9', 'A11', 'A13']);
const GREY_IDS = new Set(['H3', 'H4', 'H5']);
const INK_IDS  = new Set(['H5', 'H7']);

function applyMarkers(colors: Color[]): Color[] {
  return colors.map((c) => {
    const L = rgbToLab(hexToRgb(c.hex))[0]; // perceptual lightness
    return {
      ...c,
      isSkin: SKIN_IDS.has(c.id),
      isGrey: GREY_IDS.has(c.id),
      isInk:  INK_IDS.has(c.id),
      isDark: L < 40,
    };
  });
}

