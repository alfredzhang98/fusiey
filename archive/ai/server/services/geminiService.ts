// ──────────────────────────────────────────────────────────────────────
// Model IDs (centralised so we can swap providers in one spot).
// ──────────────────────────────────────────────────────────────────────
const MODEL_IMAGE_EDIT   = 'gemini-3.1-flash-image-preview';  // Stylize (image → image)
const MODEL_VISION_JSON  = 'gemini-3-flash-preview';          // Analyze + Evaluate (vision → JSON)
// Legacy text→image path kept for the existing /api/ai/generate-image route.
const MODEL_TEXT_TO_IMG  = 'gemini-2.5-flash-image';

let aiInstance: GoogleGenAI | null = null;
function getAI() {
  if (!aiInstance) {
    const apiKey = process.env.GEMINI_API_KEY;
    if (!apiKey) throw new Error('GEMINI_API_KEY environment variable is not set.');
    aiInstance = new GoogleGenAI({ apiKey });
  }
  return aiInstance;
}

// ──────────────────────────────────────────────────────────────────────
// Text-to-image (existing, kept for /api/ai/generate-image)
// ──────────────────────────────────────────────────────────────────────

function buildImagePrompt(userPrompt: string, paletteColors?: Color[]): string {
  let colorHint = '';
  if (paletteColors && paletteColors.length > 0) {
    const hexList = paletteColors.slice(0, 15).map((c) => c.hex).join(', ');
    colorHint = `\n- Preferred colour palette: ${hexList}`;
  }

  return `Create a pixel art image suitable for perler bead crafting.

Subject: ${userPrompt}

Requirements:
- Low resolution pixel art style (like a 32x32 or 48x48 sprite)
- Hard pixel edges with NO anti-aliasing, NO gradients, NO soft shadows
- Bold black (#000000) outlines clearly separating each colour region
- Flat solid colour fills within each region
- White (#FFFFFF) background
- Simple recognisable shapes, minimal fine detail
- Each colour region should be at least 3x3 pixels${colorHint}`;
}

export async function generateImageFromPrompt(
  prompt: string,
  paletteColors?: Color[],
): Promise<string> {
  const ai = getAI();
  const fullPrompt = buildImagePrompt(prompt, paletteColors);

  let response;
  try {
    response = await ai.models.generateContent({
      model: MODEL_TEXT_TO_IMG,
      contents: [{ text: fullPrompt }],
      config: { responseModalities: ['TEXT', 'IMAGE'] },
    });
  } catch {
    console.log(`[AI] ${MODEL_TEXT_TO_IMG} failed, falling back to ${MODEL_IMAGE_EDIT}...`);
    response = await ai.models.generateContent({
      model: MODEL_IMAGE_EDIT,
      contents: [{ text: fullPrompt }],
      config: { responseModalities: ['TEXT', 'IMAGE'] },
    });
  }

  for (const part of response.candidates?.[0]?.content?.parts || []) {
    if (part.inlineData) {
      return `data:${part.inlineData.mimeType || 'image/png'};base64,${part.inlineData.data}`;
    }
  }
  throw new Error('No image was generated by the model');
}

// ──────────────────────────────────────────────────────────────────────
// V1 analyzer — retained for backwards compatibility with the old
// /api/ai/analyze endpoint. New pipeline uses analyzeDesignV2 instead.
// ──────────────────────────────────────────────────────────────────────

export async function analyzeDesignIntent(
  prompt: string,
  imageData?: string,
): Promise<AIAnalysis> {
  const ai = getAI();

  const response = await ai.models.generateContent({
    model: MODEL_VISION_JSON,
    contents: imageData
      ? [
          { text: `Analyze this image for a perler bead pattern. User request: ${prompt}. Return a structured design specification as JSON.` },
          { inlineData: { mimeType: 'image/png', data: imageData.replace(/^data:image\/\w+;base64,/, '') } },
        ]
      : [{ text: `Create a perler bead pattern specification for: ${prompt}. Return a structured design specification as JSON.` }],
    config: {
      responseMimeType: 'application/json',
      responseSchema: {
        type: Type.OBJECT,
        properties: {
          subject: { type: Type.STRING },
          style: { type: Type.STRING },
          recommended_palette_family: { type: Type.STRING },
          recommended_max_colors: { type: Type.NUMBER },
          focus_regions: { type: Type.ARRAY, items: { type: Type.STRING } },
          simplification_rules: { type: Type.ARRAY, items: { type: Type.STRING } },
          notes: { type: Type.STRING },
        },
        required: [
          'subject', 'style', 'recommended_palette_family',
          'recommended_max_colors', 'focus_regions', 'simplification_rules', 'notes',
        ],
      },
    },
  });

  return AIAnalysisSchema.parse(JSON.parse(response.text || '{}'));
}

