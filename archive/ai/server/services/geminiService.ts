const EVAL_SCHEMA = {
  type: Type.OBJECT,
  properties: {
    confidence: { type: Type.NUMBER },
    issue: {
      type: Type.STRING,
      enum: ['too_grey', 'fragmented', 'lost_eyes', 'weak_edges', 'wrong_colors', 'unrecognizable'],
    },
    tuningHint: {
      type: Type.OBJECT,
      properties: {
        edgeBias: { type: Type.NUMBER },
        minRegion: { type: Type.NUMBER },
        mergeThreshold: { type: Type.NUMBER },
        greyTolerance: { type: Type.NUMBER },
      },
    },
    reason: { type: Type.STRING },
  },
  required: ['confidence', 'reason'],
};

/**
 * Score a bead-grid render against the original subject.
 * Used by the retry orchestrator — on low confidence, tuningHint feeds back
 * into Engine T for a second pass.
 */
export async function evaluateGrid(args: {
  gridPngBase64: string;
  subject: string;
  currentTuning: { edgeBias: number; minRegion: number; mergeThreshold: number; greyTolerance: number };
}): Promise<EvaluateResponse> {
  const ai = getAI();
  const { gridPngBase64, subject, currentTuning } = args;

  const prompt = `Given a perler-bead grid render and the original subject "${subject}".

Rate 0-1: does this render recognizably depict ${subject}?

If rating < 0.7, identify the SINGLE worst issue:
- too_grey          | too many neutral greys → raise greyTolerance (currently ${currentTuning.greyTolerance})
- fragmented        | speckled, too many islands → raise minRegion (currently ${currentTuning.minRegion})
- lost_eyes         | facial features gone → lower minRegion in eye region
- weak_edges        | outlines indistinct → raise edgeBias (currently ${currentTuning.edgeBias})
- wrong_colors      | colours shifted wrong hue → tighten paletteSubset
- unrecognizable    | cannot identify

Suggest one or two numeric tweaks as tuningHint (partial TuningSeeds).
Return JSON: { confidence, issue?, tuningHint?, reason }`;

  const response = await ai.models.generateContent({
    model: MODEL_VISION_JSON,
    contents: [
      { text: prompt },
      {
        inlineData: {
          mimeType: 'image/png',
          data: gridPngBase64.replace(/^data:image\/\w+;base64,/, ''),
        },
      },
    ],
    config: {
      responseMimeType: 'application/json',
      responseSchema: EVAL_SCHEMA,
    },
  });

  return EvaluateResponseSchema.parse(JSON.parse(response.text || '{}'));
}
